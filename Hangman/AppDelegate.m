#import "AppDelegate.h"

#define NAVIGATION_BAR_BACKGROUND   @"7-forrest-menu-bar.png"
#define NAVIGATION_BAR_BACK_BUTTON  @"7-forrest-back-button.png"
#define NAVIGATION_BAR_OTHER_BUTTON @"7-forrest-bar-button.png"
#define FONT                        @"MarkerFelt-Wide"

@implementation AppDelegate

- (BOOL)application:(UIApplication *)application didFinishLaunchingWithOptions:(NSDictionary *)launchOptions
{
    // Override point for customization after application launch.
    
    // Custom background for the navigation bar
    UIImage *navBackgroundImage = [UIImage imageNamed:NAVIGATION_BAR_BACKGROUND];
    [[UINavigationBar appearance] setBackgroundImage:navBackgroundImage forBarMetrics:UIBarMetricsDefault];
    
    
    // Custom font for the navigation bar
    [[UINavigationBar appearance] setTitleTextAttributes: [NSDictionary dictionaryWithObjectsAndKeys:
                                                           [UIColor colorWithRed:255.0/255.0 green:255.0/255.0 blue:255.0/255.0 alpha:1.0], UITextAttributeTextColor,
                                                           [UIColor colorWithRed:0.0 green:0.0 blue:0.0 alpha:0.8],UITextAttributeTextShadowColor,
                                                           [NSValue valueWithUIOffset:UIOffsetMake(0, 1)],
                                                           UITextAttributeTextShadowOffset,
                                                           [UIFont fontWithName:FONT size:21.0], UITextAttributeFont, nil]];
   
    
    // Change the appearance of back button
    UIImage *backButtonImage = [[UIImage imageNamed:NAVIGATION_BAR_BACK_BUTTON] resizableImageWithCapInsets:UIEdgeInsetsMake(0, 13, 0, 6)];
    [[UIBarButtonItem appearance] setBackButtonBackgroundImage:backButtonImage forState:UIControlStateNormal barMetrics:UIBarMetricsDefault];
    
    // Change the appearance of other navigation button
    UIImage *barButtonImage = [[UIImage imageNamed:NAVIGATION_BAR_OTHER_BUTTON] resizableImageWithCapInsets:UIEdgeInsetsMake(0, 6, 0, 6)];
    [[UIBarButtonItem appearance] setBackgroundImage:barButtonImage forState:UIControlStateNormal barMetrics:UIBarMetricsDefault];
    
    return YES;
}
							
